package math.math;

public class Methods {
    public static void main(String[] args){

        System.out.println(Math.max(3, 7));
        System.out.println(Math.min(3, 7));
        System.out.println(Math.PI);
        System.out.println(Math.round(Math.PI));
        System.out.println(Math.round(6.178));
        System.out.println(Math.abs(-6.1));
        System.out.println(Math.negateExact(-1));
        System.out.println(Math.addExact(5, 8));
        System.out.println(Math.subtractExact(10, 8));
        System.out.println(Math.multiplyExact(5, 5));
        System.out.println(Math.pow(2.5, 2.5));
        System.out.println(Math.incrementExact(6));
        System.out.println(Math.decrementExact(6));
        System.out.println(Math.random());
    }
}
